# importing a module

import math

x = 3.8777
y = math.ceil(x)
print(y) #output = 4

z = math.floor(x)
print(z) #output = 3

fac = math.factorial(4)
print(fac) #output = 24

sqrt = math.sqrt(64)
print(sqrt) #output = 8

